#include <stdio.h>

int	ft_is_prime(int nb)
{
	int i;

	if (nb <= 1)
		return (0);
	else if (nb == 2)
		return (1);
	else if (nb % 2 == 0)
		return (0);
	i = 3;
	while (i * i <= nb && i < 46340)
	{
		if (nb % i == 0)
			return (0);
		i += 2;
	}
	return (1);
}
