void	ft_rev_int_tab(int *tab, int size)
{
	int temp;
	int n;

	n = 0;
	while (n < size / 2)
	{
		temp = tab[n];
		tab[n] = tab[size - n - 1];
		tab[size - n - 1] = temp;
		n++;
	}
}
