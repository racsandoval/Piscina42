#include <unistd.h>

void	ft_putnbr(int nb)
{
	char			c;
	unsigned int	div;

	div = 1000000000;
	if (nb < 0)
	{
		write(1, "-", 1);
		nb *= -1;
	}
	if (nb == 0)
	{
		write(1, "0", 1);
	}
	while (div != 0)
	{
		if (nb / div > 0)
		{
			c = ((nb / div) % 10) + 48;
			write(1, &c, 1);
		}
		div /= 10;
	}
}
